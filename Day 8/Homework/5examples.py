#5 examples of or,and

num1=8

#and operator

print(num1 >= 3 and num1 <= 10)
print(num1 >= 4 and num1 <= 8)
print(num1 >= 6 and num1 <= 7)
print(num1 >= 8 and num1<= 9)
print(num1 >= 1 and num1 <= 11)

#or operator

print(num1 >= 3 or num1 <= 10)
print(num1 >= 4 or num1 <= 8)
print(num1 >= 6 or num1 <= 7)
print(num1 >= 8 or num1<= 9)
print(num1 <= 1 or num1 >= 11)
